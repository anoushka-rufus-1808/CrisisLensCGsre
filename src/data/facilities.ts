const CURRENT_MONTH = new Date().getMonth(); // 4 = May, 5 = June

function isShortageInSeason(shortageMonth: string): boolean {
  if (!shortageMonth || shortageMonth.toUpperCase() === "N/A") return false;
  const monthIndex: Record<string, number> = {
    january: 0, february: 1, march: 2, april: 3, may: 4, june: 5,
    july: 6, august: 7, september: 8, october: 9, november: 10, december: 11,
  };
  return shortageMonth
    .toLowerCase()
    .split(",")
    .some((m) => {
      const idx = monthIndex[m.trim()];
      return idx !== undefined && Math.abs(idx - CURRENT_MONTH) <= 1;
    });
}

function hasSolelyHandpump(source: string): boolean {
  return source.trim().toLowerCase() === "handpump";
}

function hasTanker(source: string): boolean {
  return source.toLowerCase().includes("tanker");
}

